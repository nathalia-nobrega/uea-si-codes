# Escreva um programa que receba o salário de um funcionário, calcule e mostre
# o novo salário, sabendo-se que este sofreu um aumento de 25%.

salario = float(input("Qual o salário do funcionário? "))
novo_salario = salario * 1.25
print("O novo salário ao aplicar um aumento de 25% é", novo_salario)