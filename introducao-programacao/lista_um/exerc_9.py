# Escreva um programa que calcule e mostre a área de um triângulo. Sabe-se
# que: Área = (base × altura)/2.

base = float(input("Informe o valor da base do triângulo: "))
altura = float(input("Informe o valor da altura do triângulo: "))
area = (base * altura)/2
print(f"A área do triângulo de base {base} e altura {altura} é {area}")
